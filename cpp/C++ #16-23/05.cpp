#include <iostream>
using namespace std ;

int main (){
    
    char a = '~';
    char b = '&';
    char c = '%';
    char d = 'A';

    cout<< "ASCII Value of" << a << " is " << (int)a <<endl;
    cout<< "ASCII Value of" << b << " is " << (int)b <<endl;
    cout<< "ASCII Value of" << c << " is " << (int)c <<endl;
    cout<< "ASCII Value of" << d << " is " << (int)d <<endl;
    return 0 ;
}